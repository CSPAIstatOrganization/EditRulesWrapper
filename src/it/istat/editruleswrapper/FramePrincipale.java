    private void bottoneLanciaEditRulesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bottoneLanciaEditRulesActionPerformed
        StringBuffer messaggioErrore = new StringBuffer();
        if (textFieldMetadata.getText() == null || textFieldMetadata.getText().trim().equals("")) {
            messaggioErrore.append("You must specify an input metadata file \n");
        }
        if (textFieldData.getText() == null || textFieldData.getText().trim().equals("")) {
            messaggioErrore.append("You must specify an input data file \n");
        }
        if (textFieldRules.getText() == null || textFieldRules.getText().trim().equals("")) {
            messaggioErrore.append("You must specify a rules file \n");
        }
        if (messaggioErrore.length() != 0){
            finestraErrore(messaggioErrore.toString());
        }else{
            int esitoInvocazione = wrapper.invocaScript(fileMetadataPath, fileDataPath, fileRulesPath, null);
            if (esitoInvocazione == Costanti.ESITO_POSITIVO){
                JOptionPane.showMessageDialog(this, "Execution terminated with success !", "Information", JOptionPane.INFORMATION_MESSAGE);
            }else if (esitoInvocazione == Costanti.FILE_INESISTENTI){
                JOptionPane.showMessageDialog(null, "Execution terminated with errors ! \nOne or more files submitted don't exist.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if (esitoInvocazione == Costanti.INVALID_METADATA_SET){
                JOptionPane.showMessageDialog(null, "Execution terminated with errors ! \nInvalid metadata file submitted.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if (esitoInvocazione == Costanti.INVALID_DATA_OR_RULES_FILE){
                JOptionPane.showMessageDialog(null, "Execution terminated with errors ! \nInvalid data and/or rules file submitted or bad R environment config.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if (esitoInvocazione == Costanti.INVOCAZIONE_R_NON_RIUSCITA){
                JOptionPane.showMessageDialog(null, "Execution terminated with errors ! \nR invocation failed.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else if (esitoInvocazione == Costanti.ESITO_NEGATIVO){
                JOptionPane.showMessageDialog(null, "Execution terminated with errors !", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else {
                JOptionPane.showMessageDialog(null, "Execution terminated with errors !", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_bottoneLanciaEditRulesActionPerformed
    
    private String acquisisciFile() {
        fileChooser = new JFileChooser();
        int codice = fileChooser.showOpenDialog(this);
        if (codice == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            return file.toString();
        } else if (codice == JFileChooser.CANCEL_OPTION) {
            //logger.info("Comando apri annullato");
        }
        return null;
    }
    
    private void finestraErrore(String messaggio) {
        JOptionPane.showMessageDialog(this, messaggio, "ERROR", JOptionPane.ERROR_MESSAGE);
    }
    
